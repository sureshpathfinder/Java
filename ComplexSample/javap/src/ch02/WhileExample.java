class WhileExample
{
	public static void main(String args[])
	{
		int i=0;
		while(i<5)
		{
			System.out.println("While loop Example");
			i++;
		}
	}
}

/*Unlike if loop, while loop checks only condition,it will not initialize, increment/decrement operations.
But, we can perform it in the program itself.*/
