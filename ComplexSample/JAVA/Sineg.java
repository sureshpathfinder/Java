class Sineg
{
public static void main(String args[])
{
int a=0;
System.out.println("sine value is:"+Math.sin(a));
System.out.println("cos value is:"+Math.cos(a));
System.out.println("tan value is:"+Math.tan(a));
System.out.println("The ceil value is:"+Math.ceil(5.2));
System.out.println("The floor value is:"+Math.floor(5.7));
System.out.println("The remainder value is:"+Math.IEEEremainder(5,2));
System.out.println("The power of 5 raised to 2  is:"+Math.pow(5,2));
System.out.println("The rint  value is:"+Math.rint(30.6));
System.out.println("The round value is:"+Math.round(1.4));
System.out.println("absolute value of 30:"+Math.abs(30));
System.out.println("absolute value of -30:"+Math.abs(-30));
System.out.println("random value is:"+Math.random());
System.out.println("max value is:"+Math.max(5,7));
System.out.println("min value is:"+Math.min(5,7));
System.out.println("max value is:"+Math.max(-0,0));
}
}
