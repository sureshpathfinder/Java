class ForExample
{
	public static void main(String args[])
	{	
		for(int i=0;i<=5;i++)
		{
			System.out.println("This is for example");
		}
	}
}


/*for(initialization;condition;increment/decrement)
it is used to repeat the process inside the for loop till the condition satisfies.
In this Example 

	"This is for example" will be get printed 5 times.*/
