class  ArraysExample
{
	public  static void main(String args[])
	{
		int[] s=new int[5];
		for(int k=0;k<=s.length-1;k++){
			s[k]+=5;System.out.println(k);}
		for(int i=0;i<=s.length-1;i++)
			System.out.println(s[i]);

		System.out.println("The length is "+s.length);
	}
}

