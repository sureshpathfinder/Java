package func;
 public class arith
{
 
  public int add(int a,int b)
  {
    return a+b;
  }
  public int sub(int x,int y)
  {
    return x-y;
  }
  public double mul(int a,int b)
  {
    return a*b;
  }
  public double div(int a,int b)
  {
    return a/b;
  }
  public int mod(int a,int b)
  {
    return a%b;
  }
  
  
}